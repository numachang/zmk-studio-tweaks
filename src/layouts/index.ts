// =============================================================================
// Host keyboard layout overlay
// =============================================================================
// A `HostLayout` is a thin per-HID override on top of `src/keycodes.ts`.
// It changes what we *label* a key as, not the HID code that gets sent.
// (ZMK firmware speaks HID; the host OS turns that into a character based
// on its own layout. If you set Studio's host layout to JIS, you're telling
// Studio "show me labels that match what my OS will type" — bindings stay
// the same.)
//
// Adding a layout:
//   1. Drop a `<lang>.ts` exporting a `Map<hidCode, Partial<...>>`.
//      Most files are generated by `scripts/import-layouts.py` from
//      upstream QMK keymap_<lang>.h-style sources; hand-written files
//      (ko, jis additions) live alongside them.
//   2. Add it to `LAYOUTS` below. The picker, the keycap renderer, and
//      the dropdown in AppHeader pick it up automatically.
// =============================================================================

