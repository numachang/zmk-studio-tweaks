/**
 * Per-HID label overrides. Only the fields that DIFFER from the base SSOT
 * need to be listed; missing fields fall back to the base row.
 */
export type LayoutOverride = Partial<Pick<Keycode, "short" | "med" | "long">>;

export type LayoutOverrideMap = ReadonlyMap<number, LayoutOverride>;

export interface HostLayout {
  /** Stable ID used in localStorage. Don't change after release. */
  id: string;
  /** Human-readable name for the picker (native script + English). */
  name: string;
  /** Per-HID overrides; empty for the identity (US ANSI = base SSOT). */
  overrides: LayoutOverrideMap;
}

